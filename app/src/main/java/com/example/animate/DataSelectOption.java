package com.example.animate;

public class DataSelectOption {
    private String instruction;
    private String pathIllustration;
    private String pathOp1;
    private String pathOp2;
    private String pathOp3;
    private int rightOp;

    public void setInstruction(String instruction) {
        this.instruction = instruction;
    }
    public String getInstruction() {
        return this.instruction;
    }

    public void setPathIllustration(String pathIllustration) {
        this.pathIllustration = pathIllustration;
    }
    public String getPathIllustration(){
        return this.pathIllustration;
    }

    public void setPathOp1(String pathOp1) {
        this.pathOp1 = pathOp1;
    }
    public String getPathOp1() {
        return this.pathOp1;
    }

    public void setPathOp2(String pathOp2) {
        this.pathOp2 = pathOp2;
    }
    public String getPathOp2() {
        return this.pathOp2;
    }

    public void setPathOp3(String pathOp3) {
        this.pathOp3 = pathOp3;
    }
    public String getPathOp3() {
        return this.pathOp3;
    }

    public void setRightOp(int rightOp) {
        this.rightOp = rightOp;
    }
    public int getRightOp() {
        return this.rightOp;
    }
}
